public class Item {
    private String name;
    private double price;
    
    public Item(String name, double price){
        this.name = name;
        this.price = price;
    }
    
    // Name Getter
    public String getName() {
        return name;
    }

    // Name Setter
    public void setName(String name){
        this.name = name;
    }

    // Price Getter
    public double getPrice() {
        return price;
    }

    // Price Setter
    public void setPrice(double price){
        this.price = price;
    }


}




